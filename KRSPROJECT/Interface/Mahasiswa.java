    private void Tambah_BtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Tambah_BtnActionPerformed
        // TODO add your handling code here:
        if (NIM_TF.getText().equals("")){
            JOptionPane.showMessageDialog(null,"NIM Tidak Boleh Kosong");
        } else if (Nama_TF.getText().equals("")){
            JOptionPane.showMessageDialog(null,"NAMA Tidak Boleh Kosong");
        } else if (Kelas_TF.getText().equals("")){
            JOptionPane.showMessageDialog(null,"KELAS Tidak Boleh Kosong");
        } else if (Jurusan_TF.getText().equals("")){
            JOptionPane.showMessageDialog(null,"PROGRAM STUDI Tidak Boleh Kosong");
        } else if (SMT_Box.getSelectedItem().equals("Select Semester")){
            JOptionPane.showMessageDialog(null, "SEMESTER Harus Dipilih");
        } else {
            String sql = "insert into tb_mahasiswa values (?,?,?,?,?)";
            try{
                PreparedStatement stat = conn.prepareStatement(sql);
                stat.setString(1, NIM_TF.getText());
                stat.setString(2, Nama_TF.getText());
                stat.setString(3, Kelas_TF.getText());
                stat.setString(4, Jurusan_TF.getText());
                stat.setString(5, (String)SMT_Box.getSelectedItem());
                stat.executeUpdate();
                JOptionPane.showMessageDialog(null,"Data Berhasil Disimpan");
                kosong();
                dataTable();
                lebarKolom();
                NIM_TF.requestFocus();
            } catch (SQLException e){
                JOptionPane.showMessageDialog(null, "Data Gagal Disimpan"+ e);
            }
        }
    }//GEN-LAST:event_Tambah_BtnActionPerformed

