    public void dataTable(){
        Object [] Baris = {"NIM","NAMA LENGKAP","KELAS","JURUSAN","SEMESTER"};
        tabmode = new DefaultTableModel (null, Baris);
        Mhs_TB.setModel(tabmode);
        String sql = "select*from tb_mahasiswa order by nim asc";
        try{
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while (hasil.next()){
                String NIM = hasil.getString("nim");
                String Nama = hasil.getString("nama");
                String Kelas = hasil.getString("kelas");
                String Jurusan = hasil.getString("jurusan");
                String Semester = hasil.getString("semester");
                String [] data = {NIM,Nama,Kelas,Jurusan,Semester};
                tabmode.addRow(data);
            }
           }catch (Exception e){
            
        }
        
    }
    
    public void pencarian (String sql){
        Object [] Baris = {"NIM","NAMA LENGKAP","KELAS","JURUSAN","SEMESTER"};
        tabmode = new DefaultTableModel (null,Baris);
        Mhs_TB.setModel(tabmode);
        int brs = Mhs_TB.getRowCount();
        for (int i = 0;1<brs;i++){
            tabmode.removeRow(i);
        }
        try{
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while (hasil.next()){
                String NIM = hasil.getString("nim");
                String Nama = hasil.getString("nama");
                String Kelas = hasil.getString("kelas");
                String Jurusan = hasil.getString("jurusan");
                String Semester = hasil.getString("semester");
                String [] data = {NIM,Nama,Kelas,Jurusan,Semester};
                tabmode.addRow(data);
               lebarKolom();
            }
            
        } catch (Exception e){
            
        }
    }


